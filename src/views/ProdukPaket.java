    private void bTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTambahActionPerformed
        // TODO add your handling code here:
        String kode = id.getText();
        Integer same_kode = 0;
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            if(kode.equals(table.getModel().getValueAt(i, 0).toString())){
                same_kode = 1;
                break;
            }
        }

        if(same_kode == 0){
            String ID = id.getText().toUpperCase();
            String Nama = nama.getText();
            String Harga = harga.getText();
            String HargaPaket = hargaPaket.getText();
            if(!(ID.equals("")) && !(Nama.equals(""))) {
                setTotalHarga += (int) Double.parseDouble(Harga);
                Object[] row = { ID, Nama, Harga};
                model.addRow(row);
                id.setText("");
                nama.setText("--");
                harga.setText("");
                id.requestFocus();
            }else{
                JOptionPane.showMessageDialog(null, "Terdapat inputan yang kosong.");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Kode barang sudah pernah ditambah.");
            id.setText("");
            nama.setText("--");
            harga.setText("");
            id.requestFocus();
        }
    }//GEN-LAST:event_bTambahActionPerformed

