    private void insert() {
        String idPaket = idProdukPaket.getText();
        String namaBundle = namaPaket.getText();
        double hargaBundle = Double.parseDouble(hargaPaket.getText());

        List<Product> isiProduk = new ArrayList<>();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int rowCount = model.getRowCount();

        double totalHargaNormal = 0;
        for (int i = 0; i < rowCount; i++) {
            String id = model.getValueAt(i, 0).toString();
            Product produk = ProductService.getProduk(id);
            if (produk != null) {
                isiProduk.add(produk);
                totalHargaNormal += produk.getHarga();
            }
        }

        // Hitung diskon sebagai selisih total harga satuan - harga paket
        double diskon = totalHargaNormal - hargaBundle;

        // Buat objek BundleProduct
        BundleProduct bundle = new BundleProduct(idPaket, namaBundle, hargaBundle, diskon, isiProduk);

        boolean success = ProductService.saveBundle(bundle);

        if (success) {
            JOptionPane.showMessageDialog(this, "Paket berhasil disimpan!");
            reset();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan paket!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
